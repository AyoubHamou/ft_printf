#include "ft_printf.h"

void	ft_putstr(char *s, int *size)
{
	if (!s)
	{
		ft_putstr ("(null)", size);
		return ;
	}
	while (*s)
	{
		ft_putchar(*s, size);
		s++;
	}
}
